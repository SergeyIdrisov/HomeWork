s = list(str(input()))
varible = []
for i in range(len(s)):
    if s[i] == '(' or s[i] == '{' or s[i] == '[':
        varible.append(s[i])
    if s[i] == ')' and varible[-1] == '(':
        varible.pop(-1)
    if s[i]== '}' and varible[-1] == '{':
        varible.pop(-1)
    if s[i] == ']' and varible[-1] == '[':
        varible.pop(-1)
if len(varible) == 0:
    print('Yes')
else:
    print('No')
